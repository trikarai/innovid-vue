<template>
  <v-container grid-list-xs>
    <v-row>
      <v-col style="max-width:248px !important" cols="12" md="4" lg="3" xs="12">
        <v-btn color="primary" router :to="'/incubatee/team/' + $route.params.teamId + '/invite'">
          <v-icon left>email</v-icon>Invite New Members
        </v-btn>
      </v-col>
      <v-col cols="12" md="4" lg="4" xs="12">
        <v-btn
          color="primary"
          router
          :to="'/incubatee/team/' + $route.params.teamId + '/candidate'"
        >
          <v-icon left>people</v-icon>View Invited Member Candidates
        </v-btn>
      </v-col>
    </v-row>
    <!-- <v-row>
      <v-col md="4" xs="12">
        <v-text-field
          v-model="search"
          append-icon="search"
          label="Search"
          single-line
          hide-details
          clearable
        ></v-text-field>
      </v-col>
    </v-row> -->
    <v-row>
      <v-col cols="12" lg="7" md="7" xs="12">
        <v-data-table
          :search="search"
          :loading="tableLoad"
          :headers="tableHeaders"
          :items="dataList.list"
          class="elevation-1"
        >
          <template v-slot:item.name="{item}">
            <v-btn
              class="elevation-0 mr-2"
              fab
              x-small
              color="primary"
              @click="openDetail(item.id)"
            >
              <v-icon>zoom_in</v-icon>
            </v-btn>
            {{item.founder.name}}
          </template>
          <template v-slot:item.action="{item}">
            <template v-if="authData.data.id !== item.founder.id">
              <v-btn small color="warning" @click="leftAct(item, 'Remove')">
                <v-icon left small>block</v-icon>Remove
              </v-btn>
            </template>
          </template>
        </v-data-table>
      </v-col>
    </v-row>

    <v-dialog v-model="dialogDelete" width="300" :persistent="true">
      <v-card :loading="tableLoad">
        <v-card-title>
          <p class="text-capitalize">{{leftAction}}</p>
        </v-card-title>
        <v-card-text>{{leftName}}</v-card-text>
        <v-card-actions>
          <div class="flex-grow-1"></div>
          <v-btn text color="green" @click="deleteAccount(leftId)">Yes</v-btn>
          <v-btn text color="red" @click="dialogDelete = false">Cancel</v-btn>
        </v-card-actions>
      </v-card>
    </v-dialog>

    <v-dialog
      v-model="dialogDetail"
      :overlay="false"
      max-width="300px"
      transition="dialog-transition"
    >
      <v-card>
        <v-card-title class="topaccent" primary-title>
              <div>
                <h3 class="headline mb-0">Member Detail</h3>
              </div>
        </v-card-title>
        <v-card-text v-if="loader">
          <v-progress-linear :indeterminate="true" color="primary"></v-progress-linear>
        </v-card-text>
        <transition name="slide-fade" mode="out-in">
          <v-card-text :key="dataSingle.founder.name"><b>Founder Name</b><br>{{dataSingle.founder.name}}</v-card-text>
        </transition>
        <transition name="slide-fade" mode="out-in">
          <v-card-text :key="dataSingle.position"><b>Position</b><br>{{dataSingle.position}}</v-card-text>
        </transition>
        <!-- <v-card-actions>
